class Solution:
    def getBiggestThree(self, grid: List[List[int]]) -> List[int]:
        N = len(grid)
        M = len(grid[0])
        values = set()

        def walk(row, col):
            values.add(grid[row][col])

            maxShift = min(col, M-1-col, row, N-1-row)
            if maxShift == 0:
                return

            for shift in range(1, maxShift+1):
                acc = 0
                for i in range(shift+1):
                    # bottom
                    acc += grid[row+i][col+(shift-i)]
                    acc += grid[row+i][col-(shift-i)]
                    # top
                    acc += grid[row-i][col+(shift-i)]
                    acc += grid[row-i][col-(shift-i)]

                acc -= grid[row+shift][col]
                acc -= grid[row-shift][col]
                acc -= grid[row][col+shift]
                acc -= grid[row][col-shift]

                values.add(acc)

        for row in range(N):
            for col in range(M):
                walk(row, col)

        values = list(values)
        values.sort(reverse=True)

        return values[:3]


